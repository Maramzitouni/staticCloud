void LoadMap(int arr[20][25],Map*map1){
    int length=map1->hauteurMap;
    int width=map1->largeurMap;
   for(int row=0;row<length;row++){
       for(int column=0;column<width;column++){
           map1->tabMap[row][column]=arr[row][column];
       }
   }
}

void initMap(Map*map1,Game*game) {
    map1->wallD= LoadTexture("../code/wall.bmp",game);
    map1->wall= LoadTexture("../code/wall.bmp",game);

    LoadMap(lvl1,map1);

    map1->src.x=map1->src.y=0;
    map1->src.w=map1->dest.w=32;
    map1->src.h=map1->dest.h=32;

    map1->dest.x=map1->dest.y=0;
}

void DrawMap(Map*map1,Game*game){
    int type=0;
    for(int row=0;row<20 ;row++){
        for(int column=0;column<25 ;column++){
            type=map1->tabMap[row][column];
            map1->dest.x=column*32;
            map1->dest.y=row*32;

            switch(type){
                case 0: //Draw(map1->water,map1->src,map1->dest,game);
                         break;
                case 1:   Draw(map1->wallD,map1->src,map1->dest,game);
                            break;

                case 2:   Draw(map1->wall, map1->src, map1->dest, game);
                    break;
                default:
                    break;
            }
        }
    }
}


